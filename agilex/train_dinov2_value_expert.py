def _pack_real(args, output_dir: pathlib.Path, stats: dict):
    data_dir = pathlib.Path(args.data_dir)
    rewards_dir = pathlib.Path(args.rewards_dir) if args.rewards_dir else data_dir / "rewards"

    info = _load_dataset_info(data_dir)
    task = _load_task(data_dir)

    available_eps = sorted({
        int(f.stem.split("_")[1]) for f in rewards_dir.glob("episode_*_progress.npy")
    })
    if args.episodes:
        ep_start, ep_end = map(int, args.episodes.split(":"))
        available_eps = [e for e in available_eps if ep_start <= e < ep_end]

    logging.info(f"[real] Dataset: {data_dir}")
    logging.info(f"[real] Rewards: {rewards_dir}")
    logging.info(f"[real] Task: {task}")
    logging.info(f"[real] Episodes with rewards: {len(available_eps)}")

    task_segment = task.replace(" ", "_")[:50]
    t_start = time.time()

    for ep_idx in available_eps:
        rollout_id = f"rollout_{task_segment}_real_ep{ep_idx}_done"
        rollout_dir = output_dir / rollout_id
        if rollout_dir.exists() and not args.overwrite:
            logging.info(f"  [real] skip ep {ep_idx} (already packed)")
            continue

        progress_path = rewards_dir / f"episode_{ep_idx:06d}_progress.npy"
        progress_array = np.load(str(progress_path)).astype(np.float32)

        # Decode 3 cams at extract_fps
        cam_frames = {}
        for cam in REAL_CAMERAS:
            vp = _get_video_path(data_dir, ep_idx, cam)
            if not vp.exists():
                logging.warning(f"  [real] ep {ep_idx}: missing {cam}, skip")
                break
            frames = extract_frames_pyav(str(vp), fps=args.extract_fps,
                                         max_frames=args.max_frames)
            if frames.size == 0:
                logging.warning(f"  [real] ep {ep_idx}: failed to decode {cam}, skip")
                break
            cam_frames[cam] = frames
        if len(cam_frames) != len(REAL_CAMERAS):
            continue

        # Align to shortest length
        T_cam = min(f.shape[0] for f in cam_frames.values())
        for cam in cam_frames:
            cam_frames[cam] = cam_frames[cam][:T_cam]

        # Subsample progress to match extract_fps
        dataset_fps = info["fps"]
        stride = max(1, int(round(dataset_fps / args.extract_fps)))
        progress_sub = progress_array[::stride][:T_cam]
        T = min(T_cam, len(progress_sub))
        for cam in cam_frames:
            cam_frames[cam] = cam_frames[cam][:T]
        progress_sub = progress_sub[:T]

        # Stitch
        stitched = stitch_3view(
            cam_frames["cam_high"], cam_frames["cam_left_wrist"], cam_frames["cam_right_wrist"],
            layout=args.view_layout,
            target_h=args.save_h, target_w_per_view=args.save_w // 3,
        )

        # Window & save
        windows = _make_windows(
            stitched, progress_sub,
            num_clip_frames=args.num_clip_frames, stride=args.window_stride,
        )
        if not windows:
            logging.warning(f"  [real] ep {ep_idx}: no windows, skip")
            continue

        for step_i, rec in enumerate(windows):
            _write_window_npz(rollout_dir, step_i, rec, meta={
                "source": "real",
                "task": task,
                "episode_idx": ep_idx,
            })

        stats["real_rollouts"] += 1
        stats["real_clips"] += len(windows)
        elapsed = time.time() - t_start
        logging.info(
            f"  [real] ep {ep_idx}: {len(windows)} clips, "
            f"mean_value={np.mean([r['value'] for r in windows]):.3f} "
            f"(progress=[{progress_sub.min():.3f}, {progress_sub.max():.3f}]) "
            f"| elapsed {elapsed:.0f}s"
        )


def _pack_dreamdojo(args, output_dir: pathlib.Path, stats: dict):
    if not args.dreamdojo_metadata:
        raise ValueError("--dreamdojo_metadata is required for source=dreamdojo/mixed")
    meta_path = pathlib.Path(args.dreamdojo_metadata)
    with open(meta_path) as f:
        meta_json = json.load(f)
    entries = meta_json["entries"] if isinstance(meta_json, dict) else meta_json

    logging.info(f"[dd] Metadata: {meta_path}")
    logging.info(f"[dd] Entries: {len(entries)}")

    t_start = time.time()
    for ent_i, ent in enumerate(entries):
        video_path = ent["video"]
        progress = np.asarray(ent["progress"], dtype=np.float32)
        task = ent.get("task", "dreamdojo")
        src_ep = ent.get("source_episode", -1)
        src_frame = ent.get("source_frame", -1)

        task_segment = task.replace(" ", "_")[:50]
        basename = pathlib.Path(video_path).stem
        rollout_id = f"rollout_{task_segment}_dd_{basename}_done"
        rollout_dir = output_dir / rollout_id
        if rollout_dir.exists() and not args.overwrite:
            logging.info(f"  [dd] skip {basename} (already packed)")
            continue

        if not os.path.exists(video_path):
            logging.warning(f"  [dd] missing video: {video_path}, skip")
            continue

        stitched_raw = extract_frames_pyav(
            video_path, fps=args.extract_fps, max_frames=args.max_frames,
        )
        if stitched_raw.size == 0:
            logging.warning(f"  [dd] failed to decode: {video_path}, skip")
            continue

        # Resize stitched frames to save size (preserves 1:3 aspect if source is 1:3).
        stitched = resize_stitched(stitched_raw, args.save_h, args.save_w)

        T = min(stitched.shape[0], len(progress))
        stitched = stitched[:T]
        progress = progress[:T]

        if T < args.num_clip_frames:
            logging.warning(
                f"  [dd] {basename}: only {T} frames, need {args.num_clip_frames}, skip"
            )
            continue

        windows = _make_windows(
            stitched, progress,
            num_clip_frames=args.num_clip_frames, stride=args.window_stride,
        )
        if not windows:
            continue

        for step_i, rec in enumerate(windows):
            _write_window_npz(rollout_dir, step_i, rec, meta={
                "source": "dreamdojo",
                "task": task,
                "source_episode": src_ep,
                "source_frame": src_frame,
                "video": video_path,
            })

        stats["dd_rollouts"] += 1
        stats["dd_clips"] += len(windows)
        elapsed = time.time() - t_start
        logging.info(
            f"  [dd] {basename}: {len(windows)} clips, "
            f"mean_value={np.mean([r['value'] for r in windows]):.3f} "
            f"(progress=[{progress.min():.3f}, {progress.max():.3f}]) "
            f"| ({ent_i + 1}/{len(entries)}) elapsed {elapsed:.0f}s"
        )


def pack(args):
    output_dir = pathlib.Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    stats = {"real_rollouts": 0, "real_clips": 0, "dd_rollouts": 0, "dd_clips": 0}

    if args.source in ("real", "mixed"):
        if not args.data_dir:
            raise ValueError("--data_dir is required for source=real/mixed")
        _pack_real(args, output_dir, stats)

    if args.source in ("dreamdojo", "mixed"):
        _pack_dreamdojo(args, output_dir, stats)

    meta_path = output_dir / "value_clips_meta.json"
    with open(meta_path, "w") as f:
        json.dump({
            "source": args.source,
            "num_clip_frames": args.num_clip_frames,
            "window_stride": args.window_stride,
            "save_h": args.save_h,
            "save_w": args.save_w,
            "view_layout": args.view_layout,
            "stats": stats,
        }, f, indent=2)

    logging.info(f"\nPacking complete.")
    logging.info(f"  Real:      {stats['real_rollouts']} rollouts, {stats['real_clips']} clips")
    logging.info(f"  DreamDojo: {stats['dd_rollouts']} rollouts, {stats['dd_clips']} clips")
    logging.info(f"  Saved to:  {output_dir}")


