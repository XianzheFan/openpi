def _load_dataset_info(data_dir: pathlib.Path) -> dict:
    with open(data_dir / "meta" / "info.json") as f:
        return json.load(f)


def _load_task(data_dir: pathlib.Path) -> str:
    with open(data_dir / "meta" / "tasks.jsonl") as f:
        return json.loads(f.readline().strip())["task"]


def _get_video_path(data_dir: pathlib.Path, episode_index: int, camera: str) -> pathlib.Path:
    video_key = f"observation.images.{camera}"
    return (
        data_dir / "videos" / "chunk-000" / video_key
        / f"episode_{episode_index:06d}.mp4"
    )


# ============================================================================
#  Windowing + labeling
# ============================================================================

def _make_windows(
    stitched: np.ndarray, progress: np.ndarray,
    num_clip_frames: int, stride: int,
) -> list[dict]:
    """Slide window over a stitched (T, H, W, 3) video with aligned progress."""
    T = min(stitched.shape[0], len(progress))
    records = []
    for start in range(0, T - num_clip_frames + 1, stride):
        end = start + num_clip_frames
        clip = stitched[start:end]                          # (L, H, W, 3)
        clip_progress = progress[start:end]
        value = float(1.0 - np.mean(clip_progress))
        records.append({
            "start": start,
            "end": end,
            "frames": clip,
            "value": value,
            "mean_progress": float(np.mean(clip_progress)),
        })
    return records


def _write_window_npz(
    out_dir: pathlib.Path, step_i: int, rec: dict, meta: dict,
):
    out_dir.mkdir(parents=True, exist_ok=True)
    np.savez_compressed(
        out_dir / f"step_{step_i:05d}.npz",
        frames=rec["frames"],
        value=np.float32(rec["value"]),
        mean_progress=np.float32(rec["mean_progress"]),
        start=np.int32(rec["start"]),
        end=np.int32(rec["end"]),
        **{k: np.array(v) for k, v in meta.items()},
    )


