"""
Two-phase pipeline for training a DINOv2-based Value Expert on stitched
3-view videos.

Convention
----------
- Input: a single "video" where each frame is 3 agilex views stitched
  horizontally: [cam_high | cam_left_wrist | cam_right_wrist].  This matches
  the DreamDojo `agilex_3view` output format, so the same model runs on
  real episodes and DreamDojo generations without any format change.
- Per-sample unit = one clip of `num_clip_frames` (default 4) consecutive
  stitched frames + one scalar value label.
- Label convention (consumed by agilex_infer_dinov2_value_switch.py):
    value = 1 - mean(progress in clip)
  so value ~ 0 near task completion, value ~ 1 far from completion.

Data sources
------------
Supported via `pack --source {real, dreamdojo, mixed}`:

  - real       : LeRobot v2.1 dataset with Robometer rewards under
                 <data_dir>/rewards/ (same format as train_switch_head_robometer).
                 3 cam videos (cam_high / cam_left_wrist / cam_right_wrist) are
                 stitched on the fly; labels come from progress_score.
  - dreamdojo  : already-stitched videos generated by DreamDojo
                 (IMAGINAIRE_OUTPUT_ROOT=./outputs_pnp_cup_new_agilex_3view ...).
                 Requires `--dreamdojo_metadata <path>` pointing at a JSON
                 listing entries of the form:
                    {"video": "/abs/path/to/out.mp4",
                     "progress": [0.12, 0.15, ...],   # per decoded frame
                     "task": "pnp_cup",               # optional
                     "source_episode": 5,             # optional
                     "source_frame": 42}              # optional
                 Length of `progress` must match the number of decoded frames
                 (after --extract_fps resampling).
  - mixed      : pack both real and dreamdojo into the same output_dir.
                 The `train` phase simply globs all rollout_*/step_*.npz.

Usage
-----
  # Pack real episodes
  python train_dinov2_value_expert.py pack \
      --source real \
      --data_dir data/pnp_cup_0415_new_agilex \
      --output_dir data/agilex_value_clips_pnp_cup_0416

  # Pack DreamDojo generations (requires metadata json)
  python train_dinov2_value_expert.py pack \
      --source dreamdojo \
      --dreamdojo_metadata data/dreamdojo_progress_0416.json \
      --output_dir data/agilex_value_clips_pnp_cup_0416

  # Pack both at once
  python train_dinov2_value_expert.py pack \
      --source mixed \
      --data_dir data/pnp_cup_0415_new_agilex \
      --dreamdojo_metadata data/dreamdojo_progress_0416.json \
      --output_dir data/agilex_value_clips_pnp_cup_0416

  # Train
  torchrun --nproc_per_node=8 train_dinov2_value_expert.py train \
      --data_dir data/agilex_value_clips_pnp_cup_0416 \
      --val_ratio 0.1 \
      --output_dir checkpoints/dinov2_value_expert \
      --wandb_project value_expert --wandb_run_name value_expert_0416
"""

