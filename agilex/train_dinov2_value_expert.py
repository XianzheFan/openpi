def main():
    parser = argparse.ArgumentParser(
        description="Train DINOv2 Value Expert on stitched 3-view video clips."
    )
    subparsers = parser.add_subparsers(dest="command", required=True)

    # ---- pack ----
    p_pack = subparsers.add_parser(
        "pack", help="Pack real LeRobot / DreamDojo videos into per-clip npz files"
    )
    p_pack.add_argument(
        "--source", type=str, choices=["real", "dreamdojo", "mixed"], required=True,
    )
    p_pack.add_argument("--data_dir", type=str, default=None,
                        help="LeRobot v2.1 dataset dir (required for real/mixed)")
    p_pack.add_argument("--rewards_dir", type=str, default=None,
                        help="Robometer rewards dir (default: <data_dir>/rewards)")
    p_pack.add_argument("--dreamdojo_metadata", type=str, default=None,
                        help="Metadata JSON for DreamDojo videos "
                             "(required for dreamdojo/mixed)")
    p_pack.add_argument("--output_dir", type=str, required=True)
    p_pack.add_argument("--episodes", type=str, default=None,
                        help="Episode range for real source, e.g. '0:50'")
    p_pack.add_argument("--extract_fps", type=float, default=5.0,
                        help="FPS to decode videos at; progress array is subsampled to match")
    p_pack.add_argument("--max_frames", type=int, default=1024)
    p_pack.add_argument("--num_clip_frames", type=int, default=4)
    p_pack.add_argument("--window_stride", type=int, default=1,
                        help="Stride between clip starts (in extracted frames)")
    p_pack.add_argument("--save_h", type=int, default=DEFAULT_SAVE_H,
                        help="Height of saved stitched frame (must be divisible by 14)")
    p_pack.add_argument("--save_w", type=int, default=DEFAULT_SAVE_W,
                        help="Total width of saved stitched frame "
                             "(must be divisible by 14 and by 3)")
    p_pack.add_argument("--view_layout", type=str, default="top_left_right",
                        choices=["top_left_right", "top_right_left", "left_top_right"],
                        help="Order of 3-view horizontal concat (real source only)")
    p_pack.add_argument("--overwrite", action="store_true",
                        help="Repack even if rollout dir already exists")

    # ---- train ----
    p_train = subparsers.add_parser(
        "train", help="Train DINOv2ValueExpert on packed clips"
    )
    p_train.add_argument("--data_dir", type=str, required=True)
    p_train.add_argument("--val_dir", type=str, default=None)
    p_train.add_argument("--val_ratio", type=float, default=0.0)
    p_train.add_argument("--val_split_seed", type=int, default=42)
    p_train.add_argument("--output_dir", type=str,
                         default="checkpoints/dinov2_value_expert")
    p_train.add_argument("--dinov2_model", type=str, default="dinov2_vitb14",
                         choices=["dinov2_vits14", "dinov2_vitb14",
                                  "dinov2_vitl14", "dinov2_vitg14"])
    p_train.add_argument("--num_clip_frames", type=int, default=4)
    p_train.add_argument("--attn_heads", type=int, default=8)
    p_train.add_argument("--attn_layers", type=int, default=2)
    p_train.add_argument("--hidden_dim", type=int, default=512)
    p_train.add_argument("--dropout", type=float, default=0.1)
    p_train.add_argument("--freeze_backbone", action="store_true", default=True)
    p_train.add_argument("--no_freeze_backbone", dest="freeze_backbone",
                         action="store_false")
    p_train.add_argument("--batch_size", type=int, default=32)
    p_train.add_argument("--lr", type=float, default=1e-4)
    p_train.add_argument("--weight_decay", type=float, default=1e-4)
    p_train.add_argument("--epochs", type=int, default=20)
    p_train.add_argument("--num_workers", type=int, default=4)
    p_train.add_argument("--save_every", type=int, default=1)
    p_train.add_argument("--wandb_project", type=str, default=None)
    p_train.add_argument("--wandb_run_name", type=str, default=None)

    args = parser.parse_args()

    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s [%(levelname)s] %(message)s",
    )

    if args.command == "pack":
        if args.save_h % 14 != 0 or args.save_w % 14 != 0:
            raise ValueError("save_h and save_w must be divisible by 14 (DINOv2 patch size)")
        if args.save_w % 3 != 0:
            raise ValueError("save_w must be divisible by 3 (3-view stitch)")
        pack(args)
    elif args.command == "train":
        train(args)


if __name__ == "__main__":
    main()
