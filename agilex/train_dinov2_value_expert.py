REAL_CAMERAS = ["cam_high", "cam_left_wrist", "cam_right_wrist"]
DEFAULT_SAVE_H = 224
DEFAULT_SAVE_W = 672   # 14 * 48; 3x save_h for 1:3 stitched aspect


# ============================================================================
#  Model: DINOv2ValueExpert
# ============================================================================

class DINOv2ValueExpert(nn.Module):
    """
    Per-clip scalar value regressor.

      DINOv2 (frozen) per-frame features
        → temporal TransformerEncoder over L clip frames
        → mean-pool → MLP head → scalar value

    forward(video): video is (B, L, 3, H, W) uint-normalized floats in [0,1]
    score_video(video, window_size, stride): slide window over a longer video
    """

    def __init__(
        self,
        num_clip_frames: int = 4,
        dinov2_model: str = "dinov2_vitb14",
        attn_heads: int = 8,
        attn_layers: int = 2,
        hidden_dim: int = 512,
        freeze_backbone: bool = True,
        dropout: float = 0.1,
    ):
        super().__init__()
        self.num_clip_frames = num_clip_frames

        self.backbone = torch.hub.load("facebookresearch/dinov2", dinov2_model)
        self.feature_dim = self.backbone.embed_dim
        self.freeze_backbone = freeze_backbone
        if freeze_backbone:
            for p in self.backbone.parameters():
                p.requires_grad = False
            self.backbone.eval()

        self.register_buffer(
            "img_mean", torch.tensor([0.485, 0.456, 0.406]).view(1, 3, 1, 1)
        )
        self.register_buffer(
            "img_std", torch.tensor([0.229, 0.224, 0.225]).view(1, 3, 1, 1)
        )

        # Up to num_clip_frames learnable temporal positions. score_video always
        # passes exactly num_clip_frames per window, so this is the tight size.
        self.temporal_pos_emb = nn.Parameter(
            torch.zeros(num_clip_frames, self.feature_dim)
        )
        nn.init.trunc_normal_(self.temporal_pos_emb, std=0.02)

        encoder_layer = nn.TransformerEncoderLayer(
            d_model=self.feature_dim,
            nhead=attn_heads,
            dim_feedforward=self.feature_dim * 4,
            dropout=dropout,
            activation="gelu",
            batch_first=True,
            norm_first=True,
        )
        self.temporal_attn = nn.TransformerEncoder(encoder_layer, num_layers=attn_layers)

        self.head = nn.Sequential(
            nn.LayerNorm(self.feature_dim),
            nn.Linear(self.feature_dim, hidden_dim),
            nn.GELU(),
            nn.Dropout(dropout),
            nn.Linear(hidden_dim, 1),
        )

        # Input resolution target for DINOv2 (divisible by 14). Matches
        # stitched 3-view save size by default.
        self.resize_h = DEFAULT_SAVE_H
        self.resize_w = DEFAULT_SAVE_W

    def set_input_resolution(self, h: int, w: int):
        assert h % 14 == 0 and w % 14 == 0, "DINOv2 input dims must be multiples of 14"
        self.resize_h = h
        self.resize_w = w

    def _encode_frames(self, flat_imgs: torch.Tensor) -> torch.Tensor:
        """(N, 3, H, W) float in [0,1] → (N, feature_dim)."""
        flat = F.interpolate(
            flat_imgs, size=(self.resize_h, self.resize_w),
            mode="bilinear", align_corners=False,
        )
        flat = (flat - self.img_mean) / self.img_std
        if self.freeze_backbone:
            with torch.no_grad():
                feats = self.backbone(flat)
        else:
            feats = self.backbone(flat)
        return feats

    def forward(self, video: torch.Tensor) -> torch.Tensor:
        """video: (B, L, 3, H, W) → (B,) scalar value per clip."""
        B, L, C, H, W = video.shape
        if L > self.num_clip_frames:
            raise ValueError(
                f"Clip length {L} exceeds num_clip_frames={self.num_clip_frames}"
            )
        feats = self._encode_frames(video.reshape(B * L, C, H, W))
        feats = feats.reshape(B, L, -1)
        feats = feats + self.temporal_pos_emb[:L].unsqueeze(0)
        feats = self.temporal_attn(feats)
        pooled = feats.mean(dim=1)
        return self.head(pooled).squeeze(-1)

    @torch.no_grad()
    def score_video(
        self, video: torch.Tensor, window_size: int = 4, stride: int = 1,
    ) -> torch.Tensor:
        """(B, L, 3, H, W) video → (B, num_windows) values."""
        B, L = video.shape[0], video.shape[1]
        if L < window_size:
            pad = video[:, -1:].expand(B, window_size - L, -1, -1, -1)
            video = torch.cat([video, pad], dim=1)
            L = window_size
        scores = []
        for start in range(0, L - window_size + 1, stride):
            w = video[:, start : start + window_size]
            scores.append(self.forward(w))
        return torch.stack(scores, dim=1)


