def extract_frames_pyav(
    video_path: str, fps: float = 5.0, max_frames: int = 1024,
) -> np.ndarray:
    """Decode video with PyAV (AV1-compatible). Returns uint8 (T, H, W, 3)."""
    container = av.open(video_path)
    stream = container.streams.video[0]
    video_fps = float(stream.average_rate)
    frame_interval = max(1, int(round(video_fps / fps)))

    frames = []
    for i, frame in enumerate(container.decode(video=0)):
        if i % frame_interval == 0:
            frames.append(frame.to_ndarray(format="rgb24"))
            if len(frames) >= max_frames:
                break
    container.close()

    if not frames:
        return np.array([], dtype=np.uint8)
    return np.stack(frames, axis=0)


def stitch_3view(
    top: np.ndarray, left: np.ndarray, right: np.ndarray,
    layout: str = "top_left_right",
    target_h: int = DEFAULT_SAVE_H, target_w_per_view: int | None = None,
) -> np.ndarray:
    """Horizontally concatenate 3 camera frames into one stitched frame.

    Each of top/left/right is (T, H, W, 3). Heights are resized to target_h;
    widths are set to target_w_per_view (if provided) or the per-view width
    needed for a final width of DEFAULT_SAVE_W. Returns (T, target_h, 3*target_w_per_view, 3) uint8.
    """
    from PIL import Image

    if target_w_per_view is None:
        target_w_per_view = DEFAULT_SAVE_W // 3

    views = {"top": top, "left": left, "right": right}
    order = {
        "top_left_right": ["top", "left", "right"],
        "top_right_left": ["top", "right", "left"],
        "left_top_right": ["left", "top", "right"],
    }[layout]

    T = min(v.shape[0] for v in views.values())
    out = np.zeros((T, target_h, target_w_per_view * 3, 3), dtype=np.uint8)
    for t in range(T):
        stitched = np.zeros((target_h, target_w_per_view * 3, 3), dtype=np.uint8)
        for col, key in enumerate(order):
            im = Image.fromarray(views[key][t])
            im = im.resize((target_w_per_view, target_h), Image.BILINEAR)
            stitched[:, col * target_w_per_view : (col + 1) * target_w_per_view] = (
                np.asarray(im)
            )
        out[t] = stitched
    return out


def resize_stitched(frames: np.ndarray, h: int, w: int) -> np.ndarray:
    """Resize pre-stitched (T, H, W, 3) to (T, h, w, 3)."""
    from PIL import Image
    T = frames.shape[0]
    out = np.zeros((T, h, w, 3), dtype=np.uint8)
    for t in range(T):
        im = Image.fromarray(frames[t]).resize((w, h), Image.BILINEAR)
        out[t] = np.asarray(im)
    return out


