class ValueClipDataset(Dataset):
    def __init__(self, data_dir: str, files: list[str] | None = None):
        if files is not None:
            self.files = sorted(files)
        else:
            self.files = sorted(glob.glob(
                os.path.join(data_dir, "rollout_*", "step_*.npz")
            ))
        if not self.files:
            raise FileNotFoundError(f"No value-clip npz files found in {data_dir}")
        first = np.load(self.files[0], allow_pickle=True)
        self.num_clip_frames, self.h, self.w, _ = first["frames"].shape
        logging.info(
            f"ValueClipDataset: {len(self.files)} clips, "
            f"shape=({self.num_clip_frames}, {self.h}, {self.w}, 3)"
        )

    def __len__(self):
        return len(self.files)

    def __getitem__(self, idx):
        data = np.load(self.files[idx], allow_pickle=True)
        frames = data["frames"]                                # (L, H, W, 3) uint8
        video = torch.from_numpy(frames.copy()).permute(0, 3, 1, 2).float() / 255.0
        value = torch.tensor(float(data["value"]), dtype=torch.float32)
        return video, value


def _split_by_rollout(
    data_dir: str, val_ratio: float, seed: int,
) -> tuple[ValueClipDataset, ValueClipDataset]:
    files = sorted(glob.glob(os.path.join(data_dir, "rollout_*", "step_*.npz")))
    if not files:
        raise FileNotFoundError(f"No value-clip npz files found in {data_dir}")
    rollouts: dict[str, list[str]] = {}
    for f in files:
        rid = os.path.basename(os.path.dirname(f))
        rollouts.setdefault(rid, []).append(f)
    rollout_ids = sorted(rollouts.keys())
    rng = random.Random(seed)
    shuffled = rollout_ids[:]
    rng.shuffle(shuffled)
    n_val = max(1, int(round(len(shuffled) * val_ratio)))
    if n_val >= len(shuffled):
        raise ValueError(f"val_ratio={val_ratio} leaves no train rollouts")
    val_ids = set(shuffled[:n_val])
    train_files = [f for r in rollout_ids if r not in val_ids for f in rollouts[r]]
    val_files = [f for r in rollout_ids if r in val_ids for f in rollouts[r]]
    logging.info(
        f"Split by rollout (seed={seed}): "
        f"{len(rollout_ids) - n_val} train / {n_val} val rollouts "
        f"({len(train_files)} / {len(val_files)} clips)"
    )
    return (
        ValueClipDataset(data_dir, files=train_files),
        ValueClipDataset(data_dir, files=val_files),
    )


def _collate_value(batch):
    videos, values = zip(*batch)
    return torch.stack(videos), torch.stack(values)


def _setup_distributed():
    if "RANK" not in os.environ:
        return 0, 0, 1
    dist.init_process_group(backend="nccl")
    rank = dist.get_rank()
    local_rank = int(os.environ.get("LOCAL_RANK", 0))
    world_size = dist.get_world_size()
    torch.cuda.set_device(local_rank)
    return rank, local_rank, world_size


def _cleanup_distributed():
    if dist.is_initialized():
        dist.destroy_process_group()


def train(args):
    rank, local_rank, world_size = _setup_distributed()
    distributed = world_size > 1
    is_main = rank == 0
    device = torch.device(f"cuda:{local_rank}" if torch.cuda.is_available() else "cpu")
    if is_main:
        logging.info(f"Using device: {device}  (world_size={world_size})")

    if args.val_dir:
        train_dataset = ValueClipDataset(args.data_dir)
        val_dataset = ValueClipDataset(args.val_dir)
    elif args.val_ratio > 0:
        train_dataset, val_dataset = _split_by_rollout(
            args.data_dir, val_ratio=args.val_ratio, seed=args.val_split_seed,
        )
    else:
        train_dataset = ValueClipDataset(args.data_dir)
        val_dataset = None

    model = DINOv2ValueExpert(
        num_clip_frames=args.num_clip_frames,
        dinov2_model=args.dinov2_model,
        attn_heads=args.attn_heads,
        attn_layers=args.attn_layers,
        hidden_dim=args.hidden_dim,
        freeze_backbone=args.freeze_backbone,
        dropout=args.dropout,
    ).to(device)
    model.set_input_resolution(train_dataset.h, train_dataset.w)

    if distributed:
        model = DDP(model, device_ids=[local_rank], output_device=local_rank,
                    find_unused_parameters=False)
    raw_model = model.module if distributed else model

    train_sampler = DistributedSampler(train_dataset, shuffle=True) if distributed else None
    train_loader = DataLoader(
        train_dataset, batch_size=args.batch_size,
        shuffle=(train_sampler is None), sampler=train_sampler,
        num_workers=args.num_workers, pin_memory=True,
        collate_fn=_collate_value,
    )
    val_sampler = DistributedSampler(val_dataset, shuffle=False) if (distributed and val_dataset) else None
    val_loader = (
        DataLoader(
            val_dataset, batch_size=args.batch_size,
            shuffle=False, sampler=val_sampler,
            num_workers=args.num_workers, pin_memory=True,
            collate_fn=_collate_value,
        )
        if val_dataset else None
    )

    trainable = [p for p in model.parameters() if p.requires_grad]
    if is_main:
        logging.info(f"Trainable parameters: {sum(p.numel() for p in trainable):,}")

    optimizer = torch.optim.AdamW(trainable, lr=args.lr, weight_decay=args.weight_decay)
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=args.epochs)
    criterion = nn.MSELoss()

    output_dir = pathlib.Path(args.output_dir)
    if is_main:
        output_dir.mkdir(parents=True, exist_ok=True)
    if distributed:
        dist.barrier()

    best_val_mse = float("inf")

    use_wandb = is_main and wandb is not None and args.wandb_project
    if use_wandb:
        wandb.init(
            project=args.wandb_project,
            name=args.wandb_run_name,
            config={
                "dinov2_model": args.dinov2_model,
                "num_clip_frames": args.num_clip_frames,
                "attn_heads": args.attn_heads,
                "attn_layers": args.attn_layers,
                "hidden_dim": args.hidden_dim,
                "freeze_backbone": args.freeze_backbone,
                "dropout": args.dropout,
                "batch_size": args.batch_size,
                "lr": args.lr,
                "weight_decay": args.weight_decay,
                "epochs": args.epochs,
                "world_size": world_size,
                "data_dir": args.data_dir,
                "num_train_clips": len(train_dataset),
                "input_resolution": [train_dataset.h, train_dataset.w],
            },
        )

    for epoch in range(args.epochs):
        if train_sampler is not None:
            train_sampler.set_epoch(epoch)

        model.train()
        if args.freeze_backbone:
            raw_model.backbone.eval()

        total_loss, n_batches = 0.0, 0
        total_abs_err, total_count = 0.0, 0

        for video, value in train_loader:
            video = video.to(device, non_blocking=True)
            value = value.to(device, non_blocking=True)
            pred = model(video)
            loss = criterion(pred, value)

            optimizer.zero_grad()
            loss.backward()
            torch.nn.utils.clip_grad_norm_(trainable, 1.0)
            optimizer.step()

            total_loss += loss.item()
            n_batches += 1
            total_abs_err += (pred - value).abs().sum().item()
            total_count += value.numel()

        avg_train_loss = total_loss / max(n_batches, 1)
        avg_train_mae = total_abs_err / max(total_count, 1)
        scheduler.step()

        val_str = "N/A"
        avg_val_loss = None
        if val_loader is not None:
            model.eval()
            vl, vn = 0.0, 0
            vabs, vcount = 0.0, 0
            with torch.no_grad():
                for video, value in val_loader:
                    video = video.to(device, non_blocking=True)
                    value = value.to(device, non_blocking=True)
                    pred = model(video)
                    vl += criterion(pred, value).item()
                    vn += 1
                    vabs += (pred - value).abs().sum().item()
                    vcount += value.numel()
            avg_val_loss = vl / max(vn, 1)
            avg_val_mae = vabs / max(vcount, 1)
            val_str = f"mse={avg_val_loss:.4f} mae={avg_val_mae:.4f}"

            if is_main and avg_val_loss < best_val_mse:
                best_val_mse = avg_val_loss
                torch.save(raw_model.state_dict(), output_dir / "best_model.pt")
                logging.info(f"  -> Saved best model (val_mse={avg_val_loss:.4f})")

        if is_main:
            logging.info(
                f"Epoch {epoch + 1}/{args.epochs}  "
                f"train_mse={avg_train_loss:.4f} train_mae={avg_train_mae:.4f}  "
                f"val=[{val_str}]  lr={scheduler.get_last_lr()[0]:.2e}"
            )

        if use_wandb:
            log = {
                "epoch": epoch + 1,
                "train/mse": avg_train_loss,
                "train/mae": avg_train_mae,
                "lr": scheduler.get_last_lr()[0],
            }
            if val_loader is not None:
                log["val/mse"] = avg_val_loss
                log["val/mae"] = avg_val_mae
            wandb.log(log, step=epoch + 1)

        if is_main and (epoch + 1) % args.save_every == 0:
            torch.save(raw_model.state_dict(), output_dir / f"model_epoch{epoch + 1}.pt")

    if is_main:
        torch.save(raw_model.state_dict(), output_dir / "model_final.pt")
        logging.info(f"Training complete. Models saved to {output_dir}")

    if use_wandb:
        wandb.finish()
    _cleanup_distributed()


