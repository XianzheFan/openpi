def _copy_params(source_state, target_state):
    source_flat = source_state.flat_state()
    target_flat = target_state.flat_state()
    for key in target_flat:
        if key in source_flat:
            target_flat[key].value = source_flat[key].value
    return target_state


def _soft_update_params(source_state, target_state, tau: float):
    source_flat = source_state.flat_state()
    target_flat = target_state.flat_state()
    for key in target_flat:
        if key in source_flat:
            target_flat[key].value = (
                tau * source_flat[key].value + (1.0 - tau) * target_flat[key].value
            )
    return target_state


