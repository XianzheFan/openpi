"""Fully-offline RLT training — RL Token + Actor-Critic, all from offline data.

No simulator, no server, no online rollouts. One command does everything:
  1. Train the RL Token encoder-decoder (representation learning via
     autoregressive reconstruction of frozen VLA prefix embeddings).
  2. Train the Actor-Critic via offline TD3 using the learned z_rl.

Reads Parquet + MP4 data from a local directory
(e.g. data/libero/vlaw_synthetic).

Supports two data layouts:
  * VLAW synthetic (generate_vlaw_synthetic_data.py) — MP4 videos + flat parquet
  * Perturbed trajectories — embedded PNG images in parquet

Usage:
  python scripts/train_rlt_offline.py \
      --base_config pi05_libero \
      --base_checkpoint gs://openpi-assets/checkpoints/pi05_libero \
      --data_dir data/libero/vlaw_synthetic \
      --output_dir checkpoints/rlt_libero_10/offline \
      --rl_token_steps 5000 \
      --rl_steps 20000
"""

import collections
import dataclasses
import io
import json
import logging
import math
import pathlib
import time
from typing import Any

import numpy as np
import tyro

logger = logging.getLogger("rlt_offline")


